/*

  === DRAWNING A PATH ===

    To draw a path, you have to:

    1. Create a path

    2. Set different stuff for this path

    3. Close path

    4. When path is created, you can render it (fill it or stroke it or whatever)


 --- METHODS ---

 beginPath() -- creates a new path. Onced created, future drawing commands are directed
                to this path.

closePath() -- Closes path, so future drawning are again directed to the context

fill() -- draws solid shape by filling path's content area.

stroke() -- draws the shape by stroking outline (this actually draws a line)


*/
